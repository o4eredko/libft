#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char *s1_tmp;

	s1_tmp = s1;
	s1_tmp += ft_strlen(s1);
	while (*s2)
		*s1_tmp++ = *s2++;
	*s1_tmp = '\0';
	return (s1);
}

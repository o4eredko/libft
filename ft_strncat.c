#include <libft.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char *s1_tmp;

	s1_tmp = s1;
	s1_tmp += ft_strlen(s1);
	while (*s2 && n--)
		*s1_tmp++ = *s2++;
	*s1_tmp = '\0';
	return (s1);
}
